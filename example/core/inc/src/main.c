  HAL_Delay(10000);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  uint32_t ms = HAL_GetTick();
	  if (ms > till) {
		  ++mode;
		  till = ms + period;
	  }
	  switch (mode) {
	  case 0:
		  simulate_clock(ms);
		  break;
	  case 1:
		  simulate_date(ms);
		  break;
	  case 2:
		  HT1621_Hex_Digit(data++, false, 0);
		  break;
	  case 3:
		  show_letters(data++);
		  break;
	  default:
		  HT1621_Digit(data++, 0, false, 0);
		  break;
	  }
	  HAL_Delay(500);
  }
